    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
         int linhaSelecionada = tabela.getSelectedRow();
    if (linhaSelecionada >= 0) {
        Object val = tabela.getValueAt(linhaSelecionada, 0);
        int id = ((Number) val).intValue();              
        PacienteDao dao = new PacienteDao();
        Paciente paciente = dao.buscar(id);              
        CadastroPacienteJD cadastro = new CadastroPacienteJD(paciente);
        cadastro.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um paciente para editar.");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
       int linhaSelecionada = tabela.getSelectedRow();
    if (linhaSelecionada >= 0) {
        Integer id = (Integer) tabela.getValueAt(linhaSelecionada, 0); 
        PacienteDao dao = new PacienteDao();
        dao.remover(id); 
        preencherTabela(); 
        JOptionPane.showMessageDialog(this, "Paciente removido com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um paciente para remover.");
    }
    }//GEN-LAST:event_btnRemoverActionPerformed

