    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0); // limpa tabela

        for (Profissional p : dao.listar()) {
            modelo.addRow(new Object[]{
                  p.getId(),
                    p.getNome(),
                    p.getRegistro(),
                    p.getEspecialidade(),
            });
        }
    }

