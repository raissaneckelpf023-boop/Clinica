    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
         int linhaSelecionada = tabela.getSelectedRow();
    if (linhaSelecionada >= 0) {
        Object val = tabela.getValueAt(linhaSelecionada, 0);
        int id = ((Number) val).intValue();               // ajuste para long se necessário
        ProfissionalDao dao = new ProfissionalDao();
        Profissional profissional = dao.buscar(id);               // usa buscar, não buscarPorId
        CadastroProfissional cadastro = new CadastroProfissional(profissional);
        cadastro.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um paciente para editar.");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
       int linhaSelecionada = tabela.getSelectedRow();
    if (linhaSelecionada >= 0) {
        Integer id = (Integer) tabela.getValueAt(linhaSelecionada, 0); // pega o ID
        ProfissionalDao dao = new ProfissionalDao();
        dao.remover(id); // passa só o ID
        preencherTabela(); // atualiza tabela
        JOptionPane.showMessageDialog(this, "Profissional removido com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um profissional para remover.");
    }
    }//GEN-LAST:event_btnRemoverActionPerformed

